<b>Einsatz von Cookies</b><br>
    <br>
Teilweise kommen bei der Benutzung der Webseite Cookies zum Einsatz. Die meisten Browser sind so eingestellt, dass sie Cookies automatisch akzeptieren.<br>
    Sie können das Speichern von Cookies jedoch deaktivieren oder Ihren Browser so einstellen, dass er Sie benachrichtigt, sobald Cookies gesendet werden.<br>
    Bitte informieren Sie sich über Art und Weise in den Bedienungshinweisen Ihres Browser- Herstellers.<br>
Cookies sind kleine Textdateien, die auf Ihrem Rechner abgelegt werden und die Ihr Browser speichert, um Ihnen ein möglichst individuell gestaltetes Angebot präsentieren zu können.<br>
    Greifen Sie auf solch eine Webseite zu, liefert der Server des Anbieters neben der HTML-Seite dem Browser von Ihnen ggf. auch das Cookie mit zurück.<br>
    Der Browser entscheidet dann je nach den lokalen Einstellungen, ob das Cookie auf Ihrem Rechner gespeichert wird.<br>
    Cookies können als reine Textdateien nicht in das System von Ihnen eindringen, oder dort Schaden anrichten.<br>
    Insbesondere werden Cookies von Ihrem Browser so gesperrt, dass nur die Seite sie auslesen darf, die sie gesetzt hat.<br>
    Die Textdateien werden vom Server einer gewählten Webseite gesetzt und beim nächsten Besuch auf dieser Webseite von Ihrem Browser an unseren Server geschickt.<br>
    Polka Piwo Polen AG setzt Cookies für die Altersangabe und die Zählung von Seitenaufrufen.<br>
    Hierbei wird beim Nutzer eine Zufallszahl - eine Art Pseudonym - in Form des Cookies gesetzt, anhand derer der Server erkennt, dass die Anfrage vom selben Nutzer erfolgt, ohne aber Informationen über die Identität oder sonstige Daten des Nutzers zu erhalten.<br>
    Derartige Cookies haben überdies in der Regel nur eine Lebensdauer von max. 24 Stunden. Die meisten der von uns verwendeten Cookies sind so genannte „Session-Cookies“.<br>
    Sie werden nach Ende Ihres Besuchs automatisch gelöscht. Ebenso kann die Polka Piwo Polen AG ein Cookie mit langer Lebensdauer setzen, das die technischen Einstellungen ihres Browser überprüft und als erste Information beim neuen Besuch auf der Website der Polka Piwo Polen AG ausgelesen wird.<br>
